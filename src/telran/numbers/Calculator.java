package telran.numbers;

public class Calculator {
public static double sum(double op1, double op2) {
	
	return op1 + op2;
}
static public double subtract (double op1,double op2) {

	
	return op1 - op2;
}
static public double multiply (double op1, double op2) {
	
	return op1 * op2;
}
static public double divide (double op1, double op2) {
   
	
	return op1 / op2;
}
}

